    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JOptionPane ent = new JOptionPane();
        try{
            Integer item = Integer.parseInt(ent.showInputDialog(this,"E-mail:","TCT", ent.PLAIN_MESSAGE));
            ListModel model = lEmails.getModel();
            DefaultListModel model2 = new DefaultListModel();
            for(int j = 0;j<model.getSize();j++){
                model2.addElement(model.getElementAt(j));
            }
            model2.addElement(item);
            lEmails.setModel(model2);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Invalid E-mail");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void defineConfiguration(){
        
        Email.emailInterface(configurationIndutivo);
        
        configurationIndutivo.setNumFolds(Integer.parseInt(tFolds.getText()));
        configurationIndutivo.setNumReps(Integer.parseInt(tRep.getText()));
        configurationIndutivo.setNumThreads(Integer.parseInt(tNumThreads.getText()));
        
        if(cNB.isSelected()){
            configurationIndutivo.setNB(true);
        }else{
            configurationIndutivo.setNB(false);
        }
        if(cMNB.isSelected()){
            configurationIndutivo.setMNB(true);
        }else{
            configurationIndutivo.setMNB(false);
        }
        if(cJ48.isSelected()){
            configurationIndutivo.setJ48(true);
        }else{
            configurationIndutivo.setJ48(false);
        }
        if(cSMO.isSelected()){
            configurationIndutivo.setSMO(true);
        }else{
            configurationIndutivo.setSMO(false);
        }
        if(cKNN.isSelected()){
            configurationIndutivo.setKNN(true);
        }else{
            configurationIndutivo.setKNN(false);
        }
        if(cIMBHN.isSelected()){
            configurationIndutivo.setIMBHN(true);
        }else{
            configurationIndutivo.setIMBHN(false);
        }
        if(cIMBHN2.isSelected()){
            configurationIndutivo.setIMBHN2(true);
        }else{
            configurationIndutivo.setIMBHN2(false);
        }
        
        configurationIndutivo.setValidaMaisConfiante(oMaisConfiante.isSelected());
        configurationIndutivo.setValidaSomaConfianca(oSomaConfianca.isSelected());
        configurationIndutivo.setValidaSomaPonderadaConfianca(oSomaPonderadaConfianca.isSelected());
        
        configurationIndutivo.setArffVisao1(tArffView1.getText());
        configurationIndutivo.setArffVisao2(tArffView2.getText());        
        configurationIndutivo.setDirSaida(tDirOut.getText());
        
        ArrayList<Double> confsView1 = new ArrayList<Double>();
        ListModel model = lConfidences.getModel();
        for(int item = 0;item<model.getSize();item++){
            confsView1.add((Double)model.getElementAt(item));
            configurationIndutivo.setConfidence(confsView1);
        }
    }    
    
    private void bSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarActionPerformed

        defineConfiguration();
        JFileChooser save = new JFileChooser();
        save.setFileSelectionMode(save.FILES_ONLY);
        save.setDialogTitle("Save");
        save.setDialogType(save.SAVE_DIALOG);
        save.setFileFilter(new FileNameExtensionFilter("Text Categorization Tool (*.tct)", "tct"));
        save.showSaveDialog(null);

        File config = save.getSelectedFile();
        if(config == null){
            return;
        }
        String fileName = config.toString();

        FileOutputStream file;
        ObjectOutputStream obj;
        try{
            if(!fileName.endsWith(".tct")){
                fileName = fileName + ".tct";
            }
            file = new FileOutputStream(fileName);
            obj = new ObjectOutputStream(file);
            obj.writeObject(configurationIndutivo);
            obj.close();
        }catch(Exception e){
            System.err.println("Error when saving configuration object.");
            e.printStackTrace();
            System.exit(0);
        }
    }//GEN-LAST:event_bSalvarActionPerformed


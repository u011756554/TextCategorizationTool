    private void bIncluirConfianaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bIncluirConfianaActionPerformed
        JOptionPane ent = new JOptionPane();
        try {
            Integer item = Integer.parseInt(ent.showInputDialog(this, "Confianca Visão Textos:", "TCT", ent.PLAIN_MESSAGE));
            ListModel model = lConfidences.getModel();
            DefaultListModel model2 = new DefaultListModel();
            for (int j = 0; j < model.getSize(); j++) {
                model2.addElement(model.getElementAt(j));
            }
            model2.addElement(item);
            lConfidences.setModel(model2);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Incorrect value");
            e.printStackTrace();
        }
    }//GEN-LAST:event_bIncluirConfianaActionPerformed

    private void bExcluirConfiancaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bExcluirConfiancaActionPerformed
        int i = lConfidences.getSelectedIndex();
        if (i == -1) {
            return;
        }
        ListModel model = lConfidences.getModel();
        DefaultListModel model2 = new DefaultListModel();
        for (int j = 0; j < model.getSize(); j++) {
            model2.addElement(model.getElementAt(j));
        }
        model2.removeElementAt(i);
        lConfidences.setModel(model2);
    }//GEN-LAST:event_bExcluirConfiancaActionPerformed


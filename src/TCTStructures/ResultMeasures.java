package TCTStructures;

public class ResultMeasures {
    
    //Multi-Class
    private double accuracy;
    private double error;
    private double stdDev;
    private double microPrecision;
    private double microRecall;
    private double macroPrecision;
    private double macroRecall;
    private double microF1;
    private double macroF1;
    private double buildingTime;
    private double classificationTime;
    private double numIterations;
    
    //One-Class
    private double precision;
    private double recall;
    private double f1;

    public double getAccuracy() {
        return accuracy;
    }

    public void setAccuracy(double accuracy) {
        this.accuracy = accuracy;
    }

    public double getError() {
        return error;
    }

    public void setError(double error) {
        this.error = error;
    }

    public double getStdDev() {
        return stdDev;
    }

    public void setStdDev(double stdDev) {
        this.stdDev = stdDev;
    }

    public double getMicroPrecision() {
        return microPrecision;
    }

    public void setMicroPrecision(double microPrecision) {
        this.microPrecision = microPrecision;
    }

    public double getMicroRecall() {
        return microRecall;
    }

    public void setMicroRecall(double microRecall) {
        this.microRecall = microRecall;
    }

    public double getMacroPrecision() {
        return macroPrecision;
    }

    public void setMacroPrecision(double macroPrecision) {
        this.macroPrecision = macroPrecision;
    }

    public double getMacroRecall() {
        return macroRecall;
    }

    public void setMacroRecall(double macroRecall) {
        this.macroRecall = macroRecall;
    }

    public double getMicroF1() {
        return microF1;
    }

    public void setMicroF1(double microF1) {
        this.microF1 = microF1;
    }

    public double getMacroF1() {
        return macroF1;
    }

    public void setMacroF1(double macroF1) {
        this.macroF1 = macroF1;
    }

    public double getBuildingTime() {
        return buildingTime;
    }

    public void setBuildingTime(double buildingTime) {
        this.buildingTime = buildingTime;
    }

    public double getClassificationTime() {
        return classificationTime;
    }

    public void setClassificationTime(double classificationTime) {
        this.classificationTime = classificationTime;
    }

    public double getNumIterations() {
        return numIterations;
    }

    public void setNumIterations(double numIterations) {
        this.numIterations = numIterations;
    }

    public double getPrecision() {
        return precision;
    }

    public void setPrecision(double precision) {
        this.precision = precision;
    }

    public double getRecall() {
        return recall;
    }

    public void setRecall(double recall) {
        this.recall = recall;
    }

    public double getF1() {
        return f1;
    }

    public void setF1(double f1) {
        this.f1 = f1;
    }
    
    
    
}
